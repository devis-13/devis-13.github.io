const selectedItemsList = {};

let countRealiz = 0;
let countEpicentr = 0;

switchModelBlock();
changeAmount();
activeCheckbox();
writePrice();
writeLable();

function switchModelBlock() {
	const titleList = document.querySelectorAll('.door__header');

	titleList.forEach(item => {
		item.addEventListener('click', () => {
			item.parentElement.classList.toggle('active');
		});
	});


};



function calculateAmount() {

	const amountEpicentr = document.querySelector('.door__amount-res--epicentr > span');
	const amountRealiz = document.querySelector('.door__amount-res--realiz > span');

	countRealiz = 0;
	countEpicentr = 0;

	createSelectedItemsList();

	for (key in selectedItemsList) {
		let item = selectedItemsList[key];
		let model = item.model;
		let name = item.name;

		countRealiz += doors[model][name].priceRealiz * +item.amount;
		countEpicentr += doors[model][name].priceEpicentr * +item.amount;
	};

	amountEpicentr.innerHTML = countEpicentr;
	amountRealiz.innerHTML = countRealiz;

	function createSelectedItemsList() {
		const checkedModelsItems = document.querySelectorAll('.door__item:has(input[type="checkbox"]:checked)');

		for (key in selectedItemsList) {
			delete selectedItemsList[key];
		}

		checkedModelsItems.forEach(item => {
			selectedItemsList[item.id] = {
				model: item.parentElement.parentElement.id,
				name: item.id,
				amount: item.querySelector('input[type="number"]').value,
			};
		});

	};

};

function changeAmount() {
	const block = document.querySelector('.door');

	block.addEventListener('click', function (event) {
		let elem = event.target;
		let count;
		if (elem.closest('.door__value-btn--minus')) {
			count = +elem.parentElement.querySelector('.door__amount').value;
			elem.parentElement.querySelector('.door__amount').value = count - 1;
			calculateAmount();
			writeSelectedItems();
		}

		if (elem.closest('.door__value-btn--plus')) {
			count = +elem.parentElement.querySelector('.door__amount').value;
			elem.parentElement.querySelector('.door__amount').value = count + 1;
			calculateAmount();
			writeSelectedItems();
		}


	});


}

function activeCheckbox() {
	const item = document.querySelectorAll('.door__item');

	item.forEach(elem => {
		elem.addEventListener('click', (event) => {
			if (!event.target.closest('.door__value-btn') && !event.target.closest('.door__amount')) {
				let inputCheck = elem.querySelector('input[type="checkbox"]');
				if (inputCheck.checked) {
					inputCheck.checked = false;
					calculateAmount();
					writeSelectedItems();
				} else if (!inputCheck.checked) {
					inputCheck.checked = true;
					calculateAmount();
					writeSelectedItems();
				};


			}
		});
	});
}

function writePrice() {
	const models = document.querySelectorAll('.door__item');

	models.forEach(item => {

		const blockName = item.parentElement.parentElement.id;
		const modelName = item.id;
		const modelPrice = item.querySelector('.door__price > span');

		modelPrice.innerHTML = doors[blockName][modelName].priceRealiz;
	});

}

function writeLable() {
	const models = document.querySelectorAll('.door__item');

	models.forEach(item => {
		const blockName = item.parentElement.parentElement.id;
		const modelName = item.id;
		const doorName = item.querySelector('label');

		doorName.innerHTML = doors[blockName][modelName].title;
	});
};

function writeSelectedItems() {
	let selectedBlock = document.querySelector('.seleted-block__inner');
	let selectedItemsHtmlList = {};
	selectedBlock.innerHTML = null;

	for (key in selectedItemsList) {
		let item = selectedItemsList[key];
		let model = item.model;
		let name = item.name;

		let innerText = `<div class="door__item">
									<input type="checkbox" class="door__checkbox visually-hidden" name="${doors[model].name}" id="${doors[model][name]}_lb">
									<label class="door__checkbox-label" for="${doors[model].name}_lb">
									${doors[model][name].title}
									</label>
									<p class="door__price">Ціна: ${doors[model][name].priceRealiz}<span></span></p>
									<div class="door__btn-block">
											<button class="door__value-btn door__value-btn--minus">-</button>
											<input type="number" class="door__amount" value="1">
									<button class="door__value-btn door__value-btn--plus">+</button>
									</div>
								</div>`

		selectedItemsHtmlList[name] = innerText;
	};

	for (key in selectedItemsHtmlList) {
		let item = selectedItemsHtmlList[key];

		selectedBlock.innerHTML += item;
	};

};